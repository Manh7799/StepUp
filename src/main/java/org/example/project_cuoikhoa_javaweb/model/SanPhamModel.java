package org.example.project_cuoikhoa_javaweb.model;

public class SanPhamModel {
    private String tuKhoa;
    private String maChuDe;
    private String maHang;
    private String maNhaCungCap;
    private String giaTienMin;
    private String giaTienMax;

    public String getGiaTienMin() {
        return giaTienMin;
    }

    public void setGiaTienMin(String giaTienMin) {
        this.giaTienMin = giaTienMin;
    }

    public String getGiaTienMax() {
        return giaTienMax;
    }

    public void setGiaTienMax(String giaTienMax) {
        this.giaTienMax = giaTienMax;
    }

    public String getMaHang() {
        return maHang;
    }

    public void setMaHang(String maHang) {
        this.maHang = maHang;
    }

    public String getMaNhaCungCap() {
        return maNhaCungCap;
    }

    public void setMaNhaCungCap(String maNhaCungCap) {
        this.maNhaCungCap = maNhaCungCap;
    }

    public String getTuKhoa() {
        return tuKhoa;
    }

    public void setTuKhoa(String tuKhoa) {
        this.tuKhoa = tuKhoa;
    }

    public String getMaChuDe() {
        return maChuDe;
    }

    public void setMaChuDe(String maChuDe) {
        this.maChuDe = maChuDe;
    }

}
